package Introduction.Wrapper;

public class Step02_WrapperExample {

    public static void main(String[] args) {

        Integer[] numbers = new Integer[1];     // Armazena referências a objetos Integer

        numbers[0] = 10;            // Boxing
        int value = numbers[0];     // Unboxing


//        Autoboxing e auto-unboxing
//
//        Boxing: conversão automática de um tipo primitivo para seu wrapper correspondente (int -> Integer)
//        Unboxing: processo oposto de um wrapper para seu tipo primitivo (Integer -> int)
//
//        Evita a necessidade de conversões manuais, tornando o código mais limpo



    }
}
