package introducao;

public class Step2_LiteraisBasicos {


    public static void main(String[] args) {

        // Valores literais são representações direta de dados no código, exemplos:

        int numeroHabitantes = 1_000_000;
        char simboloUnico = '$';
        String sequenciaCaracteres = "Gabriel"; // Sequência de caracteres, um único caractere tambem é uma String desde que esteja encapsulado por aspas duplas ""

        System.out.println("Literal inteiro: " + numeroHabitantes);
        System.out.println("Literal caractere: " + simboloUnico);
        System.out.println("Literal String: " + sequenciaCaracteres);
    }
}
